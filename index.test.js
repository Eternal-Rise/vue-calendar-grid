import { describe, it, expect } from 'vitest';
import { useCalendarGrid } from './index.js';

describe('useCalendarGrid', () => {
  describe('weekGrid', () => {
    it.each([
      ['2020-01-01', 1, [30, 31, 1, 2, 3, 4, 5]],
      ['2020-12-31', 53, [28, 29, 30, 31, 1, 2, 3]],
      ['2021-01-01', 53, [28, 29, 30, 31, 1, 2, 3]],
      ['2021-12-31', 52, [27, 28, 29, 30, 31, 1, 2]],
      ['2022-01-01', 52, [27, 28, 29, 30, 31, 1, 2]],
      ['2022-12-31', 52, [26, 27, 28, 29, 30, 31, 1]],
      ['2023-01-01', 52, [26, 27, 28, 29, 30, 31, 1]],
      ['2023-12-31', 52, [25, 26, 27, 28, 29, 30, 31]],
      ['2024-01-01', 1, [1, 2, 3, 4, 5, 6, 7]],
      ['2024-12-31', 1, [30, 31, 1, 2, 3, 4, 5]],
      ['2025-01-01', 1, [30, 31, 1, 2, 3, 4, 5]],
      ['2025-12-31', 1, [29, 30, 31, 1, 2, 3, 4]],
      ['2026-01-01', 1, [29, 30, 31, 1, 2, 3, 4]],
      ['2026-12-31', 53, [28, 29, 30, 31, 1, 2, 3]],
      ['2027-01-01', 53, [28, 29, 30, 31, 1, 2, 3]],
      ['2027-12-31', 52, [27, 28, 29, 30, 31, 1, 2]],
      ['2028-01-01', 52, [27, 28, 29, 30, 31, 1, 2]],
      ['2028-12-31', 52, [25, 26, 27, 28, 29, 30, 31]],
      ['2029-01-01', 1, [1, 2, 3, 4, 5, 6, 7]],
      ['2029-12-31', 1, [31, 1, 2, 3, 4, 5, 6]],
      ['2030-01-01', 1, [31, 1, 2, 3, 4, 5, 6]],
      ['2030-12-31', 1, [30, 31, 1, 2, 3, 4, 5]],
    ])('returns proper grid for date=%s', (date, week, dates) => {
      const { weekGrid } = useCalendarGrid({ date });
      const weekDates = weekGrid.value.days.map((day) => day.date);
      expect(weekGrid.value.week).toBe(week);
      expect(weekDates).toEqual(dates);
    });
  });
});
